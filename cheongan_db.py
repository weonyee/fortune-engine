def interpret_relation(ilgan, target):
    """일간 기준으로 타 천간과의 관계 해석"""
    sipsung = _get_sipsung(ilgan, target)
    ilgan_info = CHEONGAN[ilgan]
    target_info = CHEONGAN[target]

    print(f"\n  {ilgan}({ilgan_info['한글']}) 일간이 {target}({target_info['한글']})을(를) 만나면")
    print(f"  ─────────────────────────────────")
    print(f"  십성: {sipsung}")
    print(f"  의미: {SIPSUNG_DESC[sipsung]}")

    # 합 체크
    pair = frozenset((ilgan, target))
    if pair in CHEONGAN_HAP:
        hap = CHEONGAN_HAP[pair]
        print(f"\n  [합] 천간합! {hap['설명']}")

    # 충 체크
    if pair in CHEONGAN_CHUNG:
        print(f"\n  [충] 천간충! {CHEONGAN_CHUNG[pair]}")


