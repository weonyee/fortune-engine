# 오행 상생상극 관계 매핑
OHENG_ORDER = ["목", "화", "토", "금", "수"]

def _get_oheng_relation(me, other):
    """오행 기준 관계: 비화/식상/재성/관성/인성"""
    idx_me = OHENG_ORDER.index(me)
    idx_other = OHENG_ORDER.index(other)
    diff = (idx_other - idx_me) % 5
    return ["비겁", "식상", "재성", "관성", "인성"][diff]


def _get_sipsung(ilgan, target):
    """일간 기준 십성(十星) 산출"""
    me = CHEONGAN[ilgan]
    other = CHEONGAN[target]
    relation = _get_oheng_relation(me["오행"], other["오행"])

    same_yin_yang = me["음양"] == other["음양"]

    SIPSUNG_MAP = {
        ("비겁", True):  "비견",
        ("비겁", False): "겁재",
        ("식상", True):  "식신",
        ("식상", False): "상관",
        ("재성", True):  "편재",
        ("재성", False): "정재",
        ("관성", True):  "편관",
        ("관성", False): "정관",
        ("인성", True):  "편인",
        ("인성", False): "정인",
    }
    return SIPSUNG_MAP[(relation, same_yin_yang)]


